package Version2.src.Model;

public class DaiHocLoadData {
    private String maTruong;
    private String tenTruong;
    private double diemSan;

    public DaiHocLoadData(String maTruong, String tenTruong, double diemSan) {
    }

    public String getMaTruong() {
        return maTruong;
    }

    public void setMaTruong(String maTruong) {
        this.maTruong = maTruong;
    }

    public String getTenTruong() {
        return tenTruong;
    }

    public void setTenTruong(String tenTruong) {
        this.tenTruong = tenTruong;
    }

    public double getDiemSan() {
        return diemSan;
    }

    public void setDiemSan(double diemSan) {
        this.diemSan = diemSan;
    }
}

